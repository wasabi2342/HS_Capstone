{
    // OnStateEnter is called when a transition starts and the state machine starts to evaluate this state
    override public void OnStateEnter(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        animator.speed = 1.0f;
        float PinkHitSpeed;

        PinkPlayerController pinkPlayerController = animator.GetComponent<PinkPlayerController>();
        
        if (pinkPlayerController.R_attackStack >= 4)
        {
            PinkHitSpeed = 1 + 0.05f * (pinkPlayerController.R_attackStack - 3);
            animator.speed *= PinkHitSpeed;
        }
    }

